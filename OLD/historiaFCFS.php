    <h1 id="sectionTitle"><span style='color:#600;'>></span> Historia FCFS</h1>
</div>
<div class="Container" >
	<strong>AGOST 1983 </strong>
	<p>És celebra l’Assemblea constituent de la FCFS, i es comunica a la Generalitat de Catalunya. </p><p>&nbsp;</p>

	<strong>GENER 1984 </strong>
	<p>El President de la FCFS sol·licita la inscripció de la mateixa a la Direcció General d’Esports de la Generalitat de Catalunya. </p><p>&nbsp;</p>
	
	<strong>JULIOL 1985 </strong>
	<p>Resolució del Director General adduint que falten requisits per poder inscriure la FCFS. </p><p>&nbsp;</p>
	
	<strong>MAIG 1986 </strong>
	<p>La FCFS és inscrita provisionalment al registre de Federacions Esportives de Catalunya amb el nº 4604. </p><p>&nbsp;</p>

	<strong>MAIG 1988 </strong>
	<p>Es prorroga per sis mesos la inscripció definitiva de la FCFS en el registre de Federacions Esportives. </p><p>&nbsp;</p>

	<strong>NOVEMBRE 1988 </strong>
	<p>Es prorroga de nou per tres mesos la inscripció definitiva de la FCFS. </p><p>&nbsp;</p>

	<strong>FEBRER 1989 </strong>
	<p>Nova prorroga de quatre mesos per la inscripció definitiva de la FCFS. </p><p>&nbsp;</p>

	<strong>MAIG 1989 </strong>
	<p>S’inscriu a la FCFS en el registre de Federacions Esportives de la Generalitat de Catalunya, imposant-li una sèrie de condicions. </p><p>&nbsp;</p>
	
	<strong>ABRIL 1990 </strong>
	<p>És desinscriu a la FCFS del registre de Federacions Esportives, per no complir les condicions imposades en Maig de 1989. </p><p>&nbsp;</p>
	
	<strong>MAIG 1990 </strong>
	<p>La FCFS inicia un procés jurídic en defensa de la mateixa, davant el Tribunal Superior de Justícia de Catalunya. </p><p>&nbsp;</p>

	<strong>JUNY 1991 </strong>
	<p>Sentencia del TSJC favorable a la FCFS, instant a la Generalitat, perquè inscribeixi a la FCFS definitivament sense condicions. </p><p>&nbsp;</p>
	<strong>MAIG 1992 </strong>
	<p>Inscripció provisional de la FCFS en compliment de la Resolució del Tribunal Suprem. </p><p>&nbsp;</p>

	<strong>JULIOL 1992 </strong>
	<p>La Generalitat interposa recurs a la sentencia del TSJC davant el Tribunal Suprem </p><p>&nbsp;</p>

	<strong>MAIG 1999 </strong>
	<p>Sentencia del Tribunal Suprem ratificant la de data de Juny 1991 del TSJC. </p><p>&nbsp;</p>

	<strong>AGOST 2001 </strong>
	<p>La FCFS suspèn les seves competicions degut al desgast polític sofert des de l’inici de la seva activitat fins a finals de la temporada 2000/2001. </p><p>&nbsp;</p>

	<strong>AGOST 2002 </strong>
	<p>La FCFS reempren les seves competicions degut a que diversos Clubs perteneixents al “Comitè de Futbol Sala”, decideixen exigir el compliment de la sentencia del Tribunal Suprem, tan aviat com tenen coneixement de la mateixa. </p><p>&nbsp;</p>

	<strong>OCTUBRE 2002 </strong>
	<p>Assemblea de la FCFS on s’elegeix una Junta Gestora encarregada de redactar uns nous estatuts i convocar eleccions. 
Es reinicien les competicions de la FCFS amb mes de 32 Clubs participants. </p><p>&nbsp;</p>

	<strong>FEBRER 2003 </strong>
	<p>Mes de 120 equips competeixen en els diversos grups de competicions oficials i adherides a la FCFS. </p><p>&nbsp;</p>

	<strong>MARÇ 2003 </strong>
	<p>Assemblees Ordinàries i Extraordinària de la FCFS en les que s’aprova l’estat de les competicions, les propostes per la fase final de la temporada 2002/2003, les bases per les competicions de la temporada 2003/2004 i els nous Estatuts de la Federació, així com l’inici del període electoral per elegir una nova Junta Directiva. </p><p>&nbsp;</p>

	<strong>MAIG 2003 </strong>
	<p>Assemblea General Extraordinària de la FCFS en la que per un defecte de forma en la convocatòria de la del mes de març, es tornen a aprovar els nous Estatuts, el nou domicili, el reglament electoral, l’acord de convocatòria d’eleccions, l’elecció de la Junta Electoral, el procediment electoral, el calendari electoral i la fixació de la seu i data de les eleccions. </p><p>&nbsp;</p>

	<strong>JUNY 2003 </strong>
	<p>Finalitza el procés electoral amb l’elecció d’en Josep Mª Zamora com a nou President de la FCFS. </p><p>&nbsp;</p>
	<p>La FCFS organitza els Campionats Nacionals de Clubs en categories Sèniors A, Sènior B, Fémines i Juvenils, en les poblacions de Malgrat, Tossa, Lloret i Vilablareix, amb la participació de Clubs de deu Comunitats Autònomes. </p><p>&nbsp;</p>
	<p>La FCFS celebra la seva Assemblea General Ordinària aproven els pressupostos de l’any 2003 així com el pla provisional de competicions per la temporada 2003/2004. </p><p>&nbsp;</p>

	<strong>JULIOL 2003 </strong>
	<p>La FCFS rep una carta de la Secretaria General de L’Esport a la que s’adjunta l’inici d’un procediment de revocació de la inscripció de la FCFS al registre de Federacions Esportives de la Generalitat de Catalunya, a petició de la Federació Catalana de Futbol. </p><p>&nbsp;</p>
	<p>Els Clubs de la FCFS reben una carta del Consell Català de l’Esport, en la que s'els demana explicacions sobre la seva relació actual amb la FCFS i la FCF. </p><p>&nbsp;</p>

	<strong>AGOST 2003 </strong>
	<p>La FCFS presenta recurs davant el Conseller de Cultura, a l'inici del procediment de revocació de la seva inscripció al registre de Federacions. </p><p>&nbsp;</p>
	
	<strong>SETEMBRE 2003 </strong>
	<p>La FCFS demana l’execució de la sentencia del tribunal suprem. </p><p>&nbsp;</p>

	<strong>OCTUBRE 2003 </strong>
	<p>La FCFS prepara la presentació d’una querella criminal contra totes aquelles persones e institucions que han perjudicat els interessos de la mateixa. </p><p>&nbsp;</p>

	<strong>DESEMBRE 2003 </strong>
	<p>La FCFS és inscrita de manera definitiva al Registre d’Entitats Esportives de la Generalitat de Catalunya, en compliment de la sentència 366/1991 del TSJC.</p><p>&nbsp;</p>
