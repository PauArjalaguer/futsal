<h3 class="Container">Oberta inscripció FCFS temporada 2011/2012</h3>

<p class="Container">La Federació Catalana de Futbol Sala-Futsal, ha obert les inscripcions per
a la temporada 2011-2012.</p>

<p class="Container">Si vols pots formar part d´una federació propera, oberta a l´opinió dels
clubs, amb una junta directiva participativa totalment renovada l´any
2009,i on tothom te cabuda.</p>

<p class="Container">És l´única federació reconeguda internacionalment, on les seves seleccions
juguen a nivell europeu i mundial.</p>

<p class="Container">Una federació que dona la possibilitat als seus clubs de jugar les dues
competicions europees amb més història: la UEFS Champions Cup (a on
participa el campió de lliga) i la Copa UEFS ( a on participa campió i
sots campió de la Copa Catalunya).</p>

<p class="Container">Una federació amb el soport absolut del departament de la Vicepresidència
de la Generalitat de Catalunya i el Consell Català de L´Esport, on es
treballa per aconseguir subvencions als clubs afiliats.</p>

<p class="Container">Una federació conscient de les dificultats econòmiques que pateixen els
clubs, per això, la Lliga Femenina és 100% subvencionada i a on les altres
categories es mantenen les tarifes de la temporada 2006/07, a més a més, a
tots els clubs que s´apuntin abans del dia 1 de juny, pagaràn el 50% del
cost de les fitxes, és a dir, a Div. Honor la fitxa costarà 50&euro; i a les
categories de Territorial i Primera Territorial 38&euro; i 35&euro;, el futsal base és 100%
subvencionada.
</p>


